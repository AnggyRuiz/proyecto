    private void btnModificarTemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarTemaActionPerformed
        try {
            if (txtCodigoTema.getText().equals("") && txtNombreTema.getText().equals(" ")) {
                JOptionPane.showMessageDialog(null, "Primero debe buscar");
            } else {
                int idTema;
                String descripcion;
                idTema = Integer.parseInt(txtCodigoTema.getText());
                descripcion = txtNombreTema.getText();

                if (controladorTema.SolicitudModificar(idTema, descripcion)) {
                    JOptionPane.showMessageDialog(this, "Modificado exitosamente");
//                limpiar();
                    listar();

                } else {
                    JOptionPane.showMessageDialog(this, "Error al modificar");
                }
            }
            listar();
        } catch (Exception e) {
        }
        combo1();
    }//GEN-LAST:event_btnModificarTemaActionPerformed

    private void btnBuscarTemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarTemaActionPerformed
        try {
            int cedula = Integer.parseInt(JOptionPane.showInputDialog("ingrese el código del tema que desea buscar"));
            ArrayList<String> tema = controladorTema.SolicitudBuscar(cedula);

            if (tema.size() > 0) {
                for (int i = 0; i < tema.size(); i++) {
                    txtCodigoTema.setText(tema.get(0));
                    txtNombreTema.setText(tema.get(1) + "");
                    txtCodigoTema.setEnabled(false);
                }

            } else {
                JOptionPane.showMessageDialog(null, "El tema no ha sido encontrado");
            }
        } catch (Exception e) {
            System.err.println(e);
        }

        combo1();
    }//GEN-LAST:event_btnBuscarTemaActionPerformed

